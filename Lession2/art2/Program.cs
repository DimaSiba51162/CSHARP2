Задача первая:

Создать массив и заполнить его целыми числами от 1 до N
(N - Произвольное натуральное число). 
Вывести элементы массива на экран.

Пример 1 
N = 4
1 2 3 4

Пример 2
Т = 6
1 2 3 4 5 6

Этапы решения задачи.
1. Ввод N
2. Создание массива
3. Заполнение массива
4. Вывод массива на экран

